# Створення словника про книги в бібліотеці
library = {
    "Книга1": {"Автор": "Автор1", "Рік": 2020, "Сторінки": 300},
    "Книга2": {"Автор": "Автор2", "Рік": 2018, "Сторінки": 250},
    "Книга3": {"Автор": "Автор3", "Рік": 2019, "Сторінки": 200},
}

# Введення користувача
назва_книги = input("Введіть назву книги: ")

# Перевірка, чи існує книга в словнику
if назва_книги in library:
    # Виведення інформації про книгу
    print(f"Інформація про книгу '{назва_книги}':")
    for key, value in library[назва_книги].items():
        print(f"{key}: {value}")
else:
    print(f"Книга з назвою '{назва_книги}' не знайдена в бібліотеці.")

