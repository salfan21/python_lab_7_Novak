# Користувач вводить число n
n = int(input("Введіть число n: "))

# Створюємо кортеж зі списку чисел
numbers = tuple([1, 5, 10, 15, 20, 25, 30])

# Створюємо новий список result за допомогою генератора списків
result = [x for x in numbers if x < n]

# Виводимо результат
print("Числа менші за", n, ":", result)

