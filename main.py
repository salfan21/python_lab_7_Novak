def додати_номер(телефонна_книга, ім_я, новий_номер):
    """Додає новий номер телефону до списку номерів контакту."""
    if ім_я in телефонна_книга:
        телефонна_книга[ім_я].append(новий_номер)
    else:
        телефонна_книга[ім_я] = [новий_номер]

# Створення початкового словника з номерами телефонів
телефонна_книга = {
    'Анна': ['123-456-789', '987-654-321'],
    'Петро': ['555-555-555'],
    'Марія': ['111-222-333', '444-555-666']
}

# Додавання нового номера для контакту 'Анна'
додати_номер(телефонна_книга, 'Анна', '111-111-111')

# Додавання нового номера для контакту 'Ігор'
додати_номер(телефонна_книга, 'Ігор', '999-888-777')

# Виведення всіх номерів телефону для всіх контактів
for ім_я, номера in телефонна_книга.items():
    print(f"{ім_я}: {', '.join(номера)}")